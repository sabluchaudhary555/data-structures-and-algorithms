class GFG {
    public static void main(String[] args) {
        // The platform will provide input
        java.util.Scanner sc = new java.util.Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        utility(a, b);
    }
    
    public static void utility(int a, int b) {
        System.out.print(a);
        System.out.print(b);
    }
}